<h3>Semi Custom Design</h3>
<div>

<b>Semi Custom design</b> is alternative to full-custom design where we use standard library components such as half adder to make full adder.Standard cell libraries are themselves designed using full-custom design techniques.<br></br>
A half adder adds two one-bit binary numbers A and B. It has two outputs, S and C (the value theoretically carried on to the next addition); the final sum is 2C + S. The simplest half-adder design, pictured on the right, incorporates an XOR gate for S and an AND gate for C. Half adders cannot be used compositely, given their incapacity for a carry-in bit.</br>
A full adder adds binary numbers and accounts for values carried in as well as out. A one-bit full adder adds three one-bit numbers, often written as A, B, and Cin; A and B are the operands, and Cin is a bit carried in (in theory from a past addition). The circuit produces a two-bit output sum typically represented by the signals Cout and S, where \mathrm{sum} = 2 \times C_{out} + S. 
Using Semi custom Design We can make full adder using two Half adders and one OR Gate.
