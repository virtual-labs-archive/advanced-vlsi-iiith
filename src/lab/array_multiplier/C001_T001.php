<p> A binary multiplier is an electronic circuit used in digital electronics, such as a computer, 
to multiply two binary numbers. It is built using binary adders. 
A combinational multiplier is a good example of how simple logic functions (gates, half
adders and full adders) can be combined to construct a much more complex function. In
particular, it is possible to construct a 4x4 combinational multiplier from an array of
AND gates, half-adders and full-adders.


