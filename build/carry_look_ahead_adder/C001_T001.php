<h3>CARRY LOOK AHEAH HEADER</h3>
<div>
<b>Carry Look Ahead Header </b>is a type of adder used in digital logic. A carry look-ahead adder improves speed by reducing the amount of time required to determine carry bits. It can be contrasted with the simpler, but usually slower, ripple carry adder for which the carry bit is calculated alongside the sum bit, and each bit must wait until the previous carry has been calculated to begin calculating its own result and carry bits (see adder for detail on ripple carry adders)is a methodology for designing integrated circuits by specifying the layout of each individual transistor and the interconnections  between them. 

