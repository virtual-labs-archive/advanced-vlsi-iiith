<div class='def_cont'>
LFSR stands for Linear Feedback Shift Register. It is basically a Shift Register with a feedback mechanism where feedback is a linear function of the outputs at various flip-flops. The main use of LFSR is to generate pseudo-random sequence starting from a seed given by the user. It is pseudo-random because once a person knows the seed and the feedback polynomial then he can predict the whole sequence. It is generally used for encryption purpose in millitary and other applications.
<br />
<br />

The circuit design for LFSR requires the D-Flipflops and XOR Gates. XOR-Gates are used for generating the linear polynomial for feedback.
<br />
<br />

Maximal LFSR is the LFSR which passes through all the possible bit-sequences given a particular number of bits. There is a particular feedback polynomial for given number of bits. 

<br />
<br />
This Experiment will let you design a LFSR. You have to give number of bits, seed and the required feedback polynomial as the input. Then you have to make the correct connections. After that you can simulate the circuit. You can also generate the transistor level schematic of the Circuit made by you. You can see some sample Circuits in Example Section.

<br />
<br />
So Go Ahead and design Some Good Sequences. Remember All Polynomials may or may not give the complete sequence. You have to find the maximal polynomial if you want to get full range.

