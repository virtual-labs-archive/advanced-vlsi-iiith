<h3>DEFINITION OF FULL CUSTOM IC DESIGN</h3>
<div>
<b>Full-custom design </b>is a methodology for designing integrated circuits by specifying the layout of each individual transistor and the interconnections  between them. <br>Alternatives to full-custom design include various forms of semi-custom design, such as the repetition of small transistor subcircuits; one such methodology is the use of standard cell libraries (standard cell libraries are themselves designed using full-custom design techniques).

