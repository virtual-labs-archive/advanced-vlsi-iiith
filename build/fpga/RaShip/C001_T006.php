<h3>Full Custom Design</h3>
<div>
<p><font size="3">
                                         If we know the basic functionality of logic gates then we can solve any logic 
                                         circuit. This is very simple and precise way to solve logical circuit. This is less time
                                         consuimng method to get the result.
                                         </p>
                                         <br>
                                         <b>(1.)<u>NOT Gate :</u></b><br><br><br>
                                         <p>
                                          The NOT gate is also known as an inverter, simply because it
                                          changes the input to its opposite (inverts it).  The NOT gate
                                          accepts only one input and the output is the opposite of the input.
                                          In other words, a low-voltage input (0) is converted to a high-voltage output (1).
                                          </p>
                                          <br><br>
                                           <img src="not1a.gif" width="100" height="100" >
                                          <p>
                                          <br>
                                          <b>(2.)<u>AND Gate :</u></b><br><br><br>
                                          The AND gate requires two inputs and has one output.
                                          The AND gate only produces an output of 1 when BOTH
                                          the inputs are a 1, otherwise the output is 0.
                                          </p>
                                          <br>
                                          <img src="and10.gif" width="200" height="100" >
                                          <p>
                                          <br>
                                          <b>(3.)<u>OR Gate :</u></b><br><br><br>
                                          The OR gate has two input lines and one output line.
                                          Basically, if either or both of the inputs are a 1,
                                          the resulting output value is a 1.  Note in the truth
                                          table, the only time the output is 0 is when both inputs are 0.<br><br>
                                          <br>
                                          <img src="or.gif" width="200" height="100" >
                                          <p>
                                          <br>
                                          <b>(4.)<u>XOR Gate :</u></b><br><br><br>
                                          The X in the XOR gate stands for "exclusive."
                                          This means that the output from this gate will be
                                          a 1 ONLY when one or the other of the inputs is a 1.
                                          Notice in the truth table that the output is a 0 if
                                          both the inputs are 1 or 0.   In other words, this
                                          is an either-or gate.<br>
                                          <img src="xor01.gif" width="200" height="100" >
                                          <p>
                                              <b>(5.)<u>Half Adder :</u></b><br><br>

                                              The half adder is an example of a simple, functional digital circuit built
                                              from two logic gates. The half adder adds to one-binary numbers(AB). the output is
                                              the sum of the two bits(S) and the carry(C).<br>
                                              <br>
                                               Note how the same two inputs are directed to two different gates. The inputs to the XOR
                                               gate are also the inputs to the AND gate. The input "wire " to the XOR gate are tied to the input
                                               wires of the same voltage.

                                               </p><br>
                                            <img src="hadder10.gif" width="300" height="200" >

                                          </p>
                                          <br><br>
                                           <b>(6.)<u>Full Adder :</u></b><br><br>
                                          <p>
                                              The full-adder circuit adds three one-bit binary numbers (C A B) and outputs two one-bit binary numbers, a sum (S) and a carry (C1).   The full-adder is usually a component in a cascade of adders, which add 8, 16, 32, etc. binary numbers.  The carry input for the full-adder circuit is from the carry output from the circuit "above" itself in the cascade.  The carry output from the full adder is fed to another full adder "below" itself in the cascade.

If you look closely, you'll see the full adder is simply two half adders joined by an OR.
                                              </p>

