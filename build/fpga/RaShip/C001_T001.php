<p>       FPGA stands for Field Programmable Gate Array.
    FPGAs contain programmable logic components called "logic blocks",
    and a hierarchy of reconfigurable interconnects that allow the blocks
    to be "wired together"â€”somewhat like many (changeable) logic gates
    that can be inter-wired in (many) different configurations .
    Logic blocks can be configured to perform complex combinational
    functions, or merely simple logic gates like AND and XOR.
</p>
<p>    FPGA devices can be based on Flash, SRAM, EEPROM or Anti-Fuse connectivity. The most successful FPGA
devices are based on SRAM. This is because all other memory types are much less dense in terms of area
than SRAM. Also some types of connectivity are One-Time Programmable (i.e. Anti-Fuse) so they are not
very flexible. SRAM based FPGAs have no maximum erase cycle limitations either.</p>

